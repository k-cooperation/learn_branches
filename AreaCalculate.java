public class AreaCalculate {
    public static void main(String[] args) {
        double pi = 3.14;
        double r1 = 2.6;
        double r2 = 3.5;
        int r3 = 6;

        double area1 = pi * r1 * r1;
        double area2 = pi * r2 * r2;
        double area3 = pi * r3 * r3;

        System.out.println("area1 is " + area1 );
        System.out.println("area2 is " + area2 );
        System.out.println("area3 is " + area3 );
    }
}
